// ===== アプリケーションの主要モジュール定義 =====

// ルーティング設定を定義するモジュール
pub mod routes;

// 各種HTTPリクエストのハンドラーを定義するモジュール
pub mod handlers;

// REST APIのエンドポイント処理を定義するモジュール（内部的なユースケース）
pub mod api;

// 外部リソースとのやり取りを担当するモジュール（例：DBアクセス）
pub mod externals;

// ドメインモデル（データ構造体やDTOなど）を定義するモジュール
pub mod models;

// 共通処理（DB接続・設定・定数など）を定義するモジュール
pub mod common;

// 汎用ユーティリティ関数（メール送信・トークン生成など）を定義するモジュール
pub mod utils;

// 独自のエラー型を定義するモジュール（用途別に分離可能）
pub mod errors;


// ===== 外部に公開する関数 =====

// 初期ルーティング関数を外部モジュールから使えるように再公開
pub use routes::init_routes;
